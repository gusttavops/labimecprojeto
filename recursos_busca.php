    <h2>Recursos de Busca</h2>
    <p>Esta seção compila ferramentas e diretrizes que podem otimizar o processo de busca por pessoas desaparecidas.</p>

    <h3>Ferramentas Digitais Recomendadas</h3>
    <ul>
        <li><strong>Busca Avançada no Sistema:</strong> Utilize filtros combinados como nome, idade, última localização conhecida, data do desaparecimento para refinar sua busca.</li>
        <li><strong>Integração com Redes Sociais:</strong> Dicas e ferramentas para disseminar informações sobre desaparecidos em plataformas como Facebook, Instagram, Twitter.</li>
        <li><strong>Bancos de Dados Externos (Sugestões):</strong> Links para outros bancos de dados nacionais ou internacionais de pessoas desaparecidas, se houver compatibilidade para consulta.</li>
    </ul>

    <h3>Diretrizes para Colaboração</h3>
    <p>A colaboração é fundamental na busca por desaparecidos. Esta área pode oferecer informações sobre como:</p>
    <ul>
        <li><strong>Reportar informações:</strong> Canal seguro para enviar pistas ou avistamentos.</li>
        <li><strong>Participar de grupos de busca:</strong> Informações sobre como se engajar em iniciativas comunitárias de busca.</li>
        <li><strong>Medidas de segurança:</strong> Orientações para quem decide participar ativamente das buscas.</li>
    </ul>

